package app;

public class massString {
    public static void main(String[] args) {
        String[] str = new String[4];
        str[0] = "Welcom 1";
        str[1] = "Welcom 2";
        str[2] = "Welcom 3";
        str[3] = "Welcom 3";
        for (String i : str) {
            System.out.println(i);
        }
    }
}